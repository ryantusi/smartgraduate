package JavaBasicsExercises;

public class PrintAnimalPattern {
	public static void main(String[] args) {
		char y = '\u00A5';
		char c = '\u00A9';
		
		System.out.println("        '__'");
		System.out.println("        (" + c + c + ")");
		System.out.println("  /=======\\/");
		System.out.printf(" / || %%%% ||%n");
		System.out.println("*  ||----||");
		System.out.println("   " +y+y +"    " +y+y);
		System.out.println("   \"\"    \"\"");

	}
}
